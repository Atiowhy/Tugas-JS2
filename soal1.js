/*10 method bawaan javascript
1. includes()
    - metode ini digunakan untuk memeriksa apakah suatu elemen terdapat dalam array atau tidak.
      metode ini mengembalikan nilai boolean (true false)
2. find()
    - metode ini digunakan untuk mencari elemen pertama dalam array yang ditentukan oleh fungsi callback.
      metode ini mengembalikan nilai elemen yang pertama di temukan, jika tidak ditemukan mengembalikan nilai undefined
3. findIndex()
    - metode ini digunakan untuk mencari indeks dari elemen pertama dalam array yang memenuhi kondisi yang ditentukan oleh fungsi callback.
      metode ini mengembalikan indeks elemen yang pertama ditemukan, jika tidak ditemukan mengembalikan nilai -1
4. some()
    - metode ini digunakan untuk memeriksa apakah setidaknya sati elemen dalam array memenuhi kondisi yang ditentukan oleh fungsi callback.
      metode ini mengembalikan nilai boolean (true false)
5. every()
    - metode ini digunakan untuk memeriksa apakah semua elemen dalam array memenuhi kondisi yang ditentukan oleh fungsi callback.
      metode ini mengembalikan nilai boolean (true false)
6. filter()
    - metode ini digunakan untuk membuat array baru dengan elemen - elemen yang memenuhi kondisi yg ditentukan oleh fungsi callback.
      metode ini mengembalikan array baru yang berisi elemen - elemen yang lulus seleksi
7. reduce()
    - metode ini digunakan untuk mereduksi (mengurangi) elemen - elemen dalam array menjadi satu nilai tunggal berdasarkan fungsi reducer yang ditentukan.
    metode ini mengembalikan nilai hasil reduksi
8. toUpperCase()
    - metode ini digunakan untuk mengubah semua karakter dalam string menjadi huruf kapital
9. toLowerCase()
    - metode ini digunakan untuk menghasilkan angka acak antara 0 (inklusif) dan 1 (eksklusif) */
